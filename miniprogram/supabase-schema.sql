create policy p_user_funds_self on public.user_funds
for all using (openid = auth.jwt() ->> 'openid')
with check (openid = auth.jwt() ->> 'openid');

drop policy if exists p_user_holdings_self on public.user_holdings;
create policy p_user_holdings_self on public.user_holdings
for all using (openid = auth.jwt() ->> 'openid')
with check (openid = auth.jwt() ->> 'openid');

drop policy if exists p_user_portfolio_summary_self on public.user_portfolio_summary;
create policy p_user_portfolio_summary_self on public.user_portfolio_summary
for all using (openid = auth.jwt() ->> 'openid')
with check (openid = auth.jwt() ->> 'openid');

